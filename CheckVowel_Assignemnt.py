# Take Input from the user
ch = input("Enter a Character: ")

# Checking for given Character is a vowel or not
if (ch == "A" or ch == "a" or ch == "E" or ch == "e" or ch == "I" or ch == "i" or ch == "O"
or ch == "o" or ch == "U" or ch == "u"):
    print(ch, " Is a Vowel")
else:
    print(ch , " Is a Consonant")
