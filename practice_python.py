# Strip Method
mystr = "   My Name is Kunjan Patel    "
print(mystr)
result = mystr.strip()
print(result)


# Remove Character from a string in Python
str1 = 'geeks for geeks'
print(str1)
 
str2 = 'ekgs'
print(str1.strip(str2))

# Remove New line from String in Python
string = "\nHello, World!\n"
new_string = string.strip()
print(new_string)
