# Inbuilt Function

x = min(10,25,35)
y = max(10,30,25)

print("Minimum Value is : ",x)
print("Maximum Value is : ",y)

x = abs(-9.25)  # returns the positive value of the specified number
print(x)

x = pow(5, 3)  # return the value of x to the power of y
print(x)

# Import math Module
import math
x = math.sqrt(81)  # Returns a Square root of a Number
print("Square Root of a Number is : ",x)

#Round a number upward to its nearest integer
x = math.ceil(1.4)

#Round a number downward to its nearest integer
y = math.floor(1.4)

print("Cell Value is : ",x)
print("Floor Value is : ",y)

# Return the Value of PI
x = math.pi
print("The Value of PI is : ",x) 
