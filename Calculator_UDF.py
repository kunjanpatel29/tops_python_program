# This function used for adding two numbers   
def addition(n1,n2):
    n3 = n1 + n2
    print("Addition is: ",n3)

# This function used for multiplying two numbers   
def multiplication(n1,n2):
    n3 = n1 * n2
    print("Multiplication is: ",n3)

# This function used for subtracting two numbers      
def substraction(n1,n2):
    n3 = n1 - n2
    print("Substraction is: ",n3)

# This function used for dividing two numbers   
def division(n1,n2):
    n3 = n1 / n2
    print("Division is: ",n3)

# This function used for modulo two numbers   
def modulo(n1,n2):
    n3 = n1 % n2
    print("Modulo is: ",n3)

